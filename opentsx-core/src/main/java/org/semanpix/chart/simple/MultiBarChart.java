                dialog.store( dialog.chart, f2, filename);

                MeasurementTable tab = new MeasurementTable();

                if ( mrs.size() > 0 ) {
                    File f = new File( folder + "/" + "TAB_" + filename + ".dat" );
                    tab.setMessReihen( mrs );
                    tab.writeToFile( f );
                }
                




